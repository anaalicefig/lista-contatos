    private String[] findUser(String matricula, String senha) throws IOException {
        FileReader fr = null;
        try {
            fr = new FileReader("usuarios.txt");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        BufferedReader br = new BufferedReader(fr);
        String line = null;
        String file;
        while ((file = br.readLine()) != null) {
            line += file + "\n";
        }

        String vet[] = line.split("\n");

        for (int i = 0; i < vet.length; i++) {
            String usuario[] = vet[i].split(";");

            if (matricula.equals(usuario[1]) && senha.equals(usuario[2])) {
                return usuario;
            }
        }                
        
        return null;
    }

    private void btnLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoginMouseClicked
        String matricula = "matricula:" + userLogin.getText();
        String senha = "password:" + userPassword.getText();        
        boolean admin = false;
        try {
            String user[] = findUser(matricula, senha);
            
            if(user[3].equals("type:admin"))
                admin = true;
            
            ListarContatos lista = new ListarContatos(admin);
            this.dispose();
            lista.setVisible(true);
            
        } catch (IOException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
               
        

    }//GEN-LAST:event_btnLoginMouseClicked

