    private boolean validarCampos() {
        if (nomeInput.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor digite um nome para se cadastrar!", "Oops", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (matriculaInput.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor digite uma Matricula para se cadastrar!", "Oops", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (passwordInput.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor digite uma senha para se cadastrar!", "Oops", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (confirmPasswordInput.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor digite uma confirmação de senha para se cadastrar!", "Oops", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if ((passwordInput.getText().length() < 4)) {
            JOptionPane.showMessageDialog(null, "Por favor digite uma senha com mais de 4 caracteres!", "Oops", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if ((passwordInput.getText().length() > 9)) {
            JOptionPane.showMessageDialog(null, "Por favor digite uma senha com menos de 9 caracteres!", "Oops", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

    private boolean userExists(String matricula) {
        Scanner scanner = null;
        try {
            scanner = new Scanner(new FileReader("usuarios.txt"));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Cadastrar.class.getName()).log(Level.SEVERE, null, ex);
        }

        while (scanner.hasNextLine()) {
            String usuario = scanner.nextLine();
            String[] vet = usuario.split(";");

            for (int i = 0; i < vet.length; i++) {
                if (matricula.equals(vet[i])) {
                    JOptionPane.showMessageDialog(null, "Já existe um usuário cadastrado com essa matrícula!", "Oops", JOptionPane.ERROR_MESSAGE);
                    return false;
                }
            }
        }
        return true;
    }

    private void btnSalvarCadastroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarCadastroMouseClicked
        String data[] = new String[4];
        data[0] = "nome:" + nomeInput.getText();
        data[1] = "matricula:" + matriculaInput.getText();
        data[2] = "password:" + passwordInput.getText();
        data[3] = "type:user";

        boolean validade = validarCampos();
        boolean notExists = userExists(data[1]);

        if (validade && notExists) {
            if (!passwordInput.getText().equals(confirmPasswordInput.getText())) {
                JOptionPane.showMessageDialog(null, "Senhas não conferem!", "Oops", JOptionPane.ERROR_MESSAGE);
            } else {
                try {
                    cadastraUsuario(data);
                    JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    Login login = new Login();
                    login.setVisible(true);
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(Cadastrar.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(Cadastrar.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnSalvarCadastroMouseClicked

