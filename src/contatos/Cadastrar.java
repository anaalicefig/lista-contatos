    private void cadastraUsuario(String[] data) throws FileNotFoundException, IOException {
        File usuarios = new File("usuarios.txt");
        FileWriter pw = new FileWriter(usuarios, true);
        for (int i = 0; i < data.length; i++) {
            if (i == data.length - 1) {
                pw.write(data[i] + ";\n");
            } else {
                pw.write(data[i] + ";");
            }
        }

        pw.close();
    }

